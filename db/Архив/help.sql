
-- =========================================================================
-- Выбор колонки по части имени
-- Иногда нужно быстро выбрать только те колонки, чьи имена содержат
-- общий префикс. Это удобно при работе с широкими таблицами.
-- Запрос берет имена колонок из системного каталога по шаблону.
-- Дальше можно собрать SELECT c нужными полями.
-- =========================================================================
SELECT column_name 
FROM information_schema.columns
WHERE table_name = 'events'
  AND column_name LIKE 'meta_%';
